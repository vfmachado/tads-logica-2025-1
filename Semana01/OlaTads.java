public class OlaTads {
    public static void main(String[] args) {
        // SISTEMA . SAIDA . PRINTAR/MOSTRAR ( " TEXTO DESEJADO ") ; 
        System.out.print("OI TADS!");   // print sem quebra de linha
        System.out.println("\tProfessor Vinicius"); // print com quebra de linha
        System.out.println("1. LOGICA\n2. JAVA\n3. TADS");
                                                    //   ^  obrigatorio
        // \n  => QUEBRA DE LINHA   (enter, pular de linha)
        System.out.println("Vinicius\n\tFritzen\n\n\t\tMachado");

        // \t  =>  tabulação (TAB) ... DA UNS ESPACINHOS =)

        // PQ ESSE TEXTO FOI IGNORADO?
        /* 
        ESTE
           TAMBÉM FOI IGNORADO?
        */
    }
}

